# 使用一个字典来存储一个熟人的信息，包括名、姓、年龄和居住的城市。该字典应包含键first_name 、last_name 、age 和city 。将存储在该字典中的每项信息都打印出来。

person = {
    'first_name': 'li',
    'last_name': 'si',
    'age': 20,
    'city': 'beijing',
}

print(person)

# 访问字典中的值
print(person['first_name'])
print(person['last_name'])
print(person['age'])
print(person['city'])
